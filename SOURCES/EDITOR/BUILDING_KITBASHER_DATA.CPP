const KitbashFaceTemplate *KitbashData_FindFace(const char *tag, S32 ordinal) {
    S32 i;

    for (i = 0; i < (S32)(sizeof(NeighborHouseFaces) / sizeof(NeighborHouseFaces[0])); i++) {
        if (strcmp(NeighborHouseFaces[i].tag, tag) == 0) {
            if (ordinal == 0)
                return &NeighborHouseFaces[i];
            ordinal--;
        }
    }

    return NULL;
}

